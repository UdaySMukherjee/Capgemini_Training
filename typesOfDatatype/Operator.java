package typesOfDatatype;

public class Operator {
	public static void main(String[] args) {
	double data1 = 10.0;
	int data2 = 10;
	System.out.println(data1>data2);
	System.out.println(data1<data2);
	System.out.println(data1>=data2);
	System.out.println(data1<=data2);
	System.out.println(data1==data2);
	System.out.println(data1!=data2);
	}
	
}
