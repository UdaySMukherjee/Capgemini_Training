package typesOfDatatype;

public class EvenOdd {
	public static void main(String[] args) {
		char a = 'B';
		System.out.println(a>=97?(a<=122?"lower":"not character"):(a>=65?(a<=90?"upper":"notcharacter"):"not character"));
		int grade = 97;
		System.out.print(grade>=90?"A+":(grade>=85?"A":(grade>=75?"B":"C"))) ;
	
	}
}
